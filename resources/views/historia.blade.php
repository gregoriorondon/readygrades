    <h1>Historia</h1>
    <p>Debido al gran déficit de cupos Universitarios el Instituto Universitario de Tecnología
del Estado Trujillo "Don Rómulo Betancourt" se generó un plan de Expansión
a Nivel del Estado, para ofrecer otra alternativa de estudios superiores y contribuir
al desarrollo potencial del Estado. Gracias a las gestiones de Carlos Simón Olmos
se logró establecer en el Dividive la Sede según la Resolución del Ministerio de
Educación Publicada en gaceta oficial el 16 de Noviembre de 1988.</p>
    <p>La Extensión del IUTET Zona Baja, inicia sus actividades académicas formando
Técnicos Superiores Universitarios en la Carrera de Administración de Empresas,
con un Programa de Inducción para los alumnos del curso Propedéutico A-89, el 3
de Abril de 1989, en la sede del Liceo “Doña Fulgencia Jiménez de Olmos”, teniendo
a su disposición dos aulas de clase y dos oficinas, dicho curso tuvo una duración de
14 semanas con una matrícula de 120 alumnos dividido en dos turnos con un horario
de 1:00pm a 6:00pm y de 6:00pm a 11:00pm para entonces la Extensión El Dividive
contaba con un (1) Coordinador Prof Vistrimo Hidalgo y siete (7) Docentes, Nohelia
Ascanio, Pbro Pedro Balza, Ines Barrios, María Pacheco, Gioconda Miele, Juan
Ramírez y Alexandra Theodorou.</p>
    <p>Igualmente Destacan como Personal Administrativo Nancy Amada Méndez,
María Auxiliadora de Duran, Bibliotecarios Edilberto Contreras, Leonardo Briceño, 
Personal Obrero Ilsa de Gil, Noris Pérez. La actividad Cultural en la Extensión
El Dividive se llevó a la par con la Fundación de la misma, bajo 
la Dirección del Ingeniero Herman Quintero, se comenzó a
trabajar con Actividades Complementarias en el año 1989 bajo la conducción del
Prof. Jesús Rodríguez Lera Profesional de la zona en el área Docente Musical. Ya
para ese mismo año (1989) se pudo contar con una Coral Universitaria integrada
por Estudiantes de la Extensión y personas de la Comunidad con aptitudes para la
actividad vocal. También se logro conformar un grupo de Teatro que hizo vida activa
dentro y fuera de la Extensión. En este inicio Cultural también se hicieron
innumerables actividades Culturales dentro de las que destacan Conciertos
Corales, Actos Protocolares, presentaciones Teatrales, y se inicio en nuestra
Extensión la Voz Iuteista.</p>
    <p>El 15 de Marzo de 1991 el Instituto Universitario de Tecnología de Estado Trujillo
Extensión El Dividive se traslada a sus nuevas Instalaciones ubicada al frente de la
Carretera Panamericana Diagonal a la Cruz de la Misión, específicamente cuando
es creada la Carrera de Mantenimiento de Maquinaria Agrícola y Pesada, la cual va
a formar por primera vez en Venezuela Técnicos Superiores en Mantenimiento de
Maquinaria Agrícola y Pesada. Entre los Docentes fundadores de esa Carrera
destacan Otto Markan, Francisco Araujo, Eligio Graterol, Francisco Olivares, Leida
Viloria, Pablo Abreu, Leonardo Salas, Coromoto Araujo, José Santos Montilla. Para
Junio de 1992 la extensión contaba con 40 Docentes y una matricula de 524
alumnos. En 1994 en vista de la Materias Practicas que se dictaban para formar a
los T.S.U en Mantenimiento de Maquinaria Agrícola y Pesada ingresan a la sede un
grupo de auxiliares docentes, entre los que destacan José Gregorio Fernández,
Pedro Mijares, Yusmar Hernández y Francisco Garces.</p>
    <p>En el mes de Octubre del 2008 el IUTET se integra al proceso de transformación de
los Institutos y Colegios Universitarios en Universidades, con la coordinación de la
Lcda. Mary Marin en el PNF de Administración y el Ingeniero José Fernández en el
PNF de Mantenimiento, lo cual implica mucho más que el establecimiento de las
llamadas carreras largas. En el centro del cambio está la intención de crear
universidades comprometidas con el país y sus ciudadanos, capaces de generar,
transformar y apropiar conocimiento socialmente útil y adecuado, que sirva de
sustento al proceso de cambio que vive la nación, reconstruyendo su base ética,
social, cultural y productiva para el logro de una sociedad justa e igualitaria.</p>
    <p>El 2 de mayo del 2014 decreto N.936 el Presidente Nicolás Maduro pública en
 Gaceta Oficial, que los IUTET pasan a ser Universidades Politécnicas, quedando nuestra
 extensión como Universidad Politécnica Territorial del Estado Trujillo (U.P.T.T) Mario
 Briceño Iragorry núcleo Francisco de Miranda, en el Marco de la Misión Ama Mater, como 
Universidad Nacional Experimental, mediante la articulación de la Misión Sucre e 
Instituciones de Educación Universitaria.</p>

